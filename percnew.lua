local function randomTeleport(root, island)

    local teleports = IslandTeleports[island.Name]
    if not teleports or #teleports == 0 then return end

    local randomCF = teleports[math.random(1, #teleports)]

    root.CFrame = randomCF
    root.AssemblyLinearVelocity = Vector3.zero
    root.AssemblyAngularVelocity = Vector3.zero
end

local function getNearestHorse(root, island)

    local nearest = nil
    local shortest = math.huge

    for _, obj in ipairs(island:GetDescendants()) do
        if obj:IsA("Model") then

            local horseRoot = obj:FindFirstChild("HumanoidRootPart")
            local captureGui = obj:FindFirstChild("CaptureProgress", true)

            if horseRoot and captureGui then
                local distance = (root.Position - horseRoot.Position).Magnitude

                if distance < shortest then
                    shortest = distance
                    nearest = horseRoot
                end
            end
        end
    end

    return nearest
end

